                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Problem with Timer</h5>
                            <p class="text-justify">If the timer is not working properly, you can check:</p>
                            <li>The lid switch</li>
                            <li>Timer motor</li>
                            <li>Water level control</li>
                            <li>Timer knob</li>
                            <li>Drain pump</li>
                            <li>The water inlet valve.</li>
                            <p class="text-justify">These are the most common parts that can cause a problem with the timer. If you are unable to fix the problem, you should call a professional.</p>
                        </div>
                    </div>
                    <div class="row" id="machine_shakes_moves">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Machine Shakes and Moves</h5>
                            <p class="text-justify">A damaged shock absorber, suspension spring, snubber ring, tub spring, pads, dampening strap, or leveling leg could cause this problem. You can start by checking the shock absorbers and the suspension spring. If these parts are in perfect condition, you can check the pads, ring, strap, and the leveling leg.</p>
                        </div>
                    </div>
                    <div class="row" id="problem_with_water_level">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Problem with Water Level</h5>
                            <p class="text-justify">You are likely to face this problem when the machine does not stop filling water or does not fill water to the proper level. To rectify this problem you must check:</p>
                            <li>The water inlet valve</li>
                            <li>Water level switch</li>
                            <li>Air dome tube</li>
                            <li>Pressure switch</li>
                            <li>Electronic controls</li>
                            <li>The inlet hoses.</li>
                            <p class="text-justify">You will need a multi-meter to check electronic components such as switch and electric board. If any part is worn out or damaged, replace it immediately to rectify this problem.</p>
                        </div>

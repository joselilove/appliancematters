                            <h5 class="font-weight-bold">Microwave Stops Working </h5>
                            <p class="text-justify">If the microwave stops working, there could be a problem in the fuse or the door switch.
                                The fuse is a safety device that protects the microwave from strong current. It stops the flow of current to the microwave
                                when the current is too strong. If there is a problem with the fuse, the appliance will not work until the fuse is replaced.
                                When there is a problem with the door switch, you must get it replaced. To replace the switch, you will need professional help.</p>
                        </div>

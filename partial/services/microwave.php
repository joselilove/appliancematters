                            <h5 class="font-weight-bold">Carousel cooking tray not turning</h5>
                            <p class="text-justify">The carousel tray may not turn because it is not lined up properly.
                                To rectify this problem, you must remove the debris caught in it and realign the tray.
                                If this does not solve the problem, you can check for problems in the drive bush. You may have to replace the
                                drive bush if it is damaged or broken. At times, the problem in the tray motor can cause this problem.
                                To check the condition of the motor, it is best to get technical help. The microwave open can give a serious
                                shock even when you unplug it. For your safety and the safety of your family members, you must get professional
                                help to repair the motor.</p>

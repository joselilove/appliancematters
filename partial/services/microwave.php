                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Sparks inside the Microwave</h5>
                            <p>If you notice sparks inside the microwave, it could be due to problems in the internal cavity, diode, or the stirrer.
                                When food gets accumulated in the internal cavity of the microwave, it can cause sparks.
                                To rectify this problem, you must clean your microwave thoroughly. When there is a problem with the stirrer,
                                the energy of the microwave will concentrate on one area. Problem in the stirrer may be due to broken fan motor or
                                damaged stirrer belt. Replacing the stirrer belt or fan motor is a complex procedure that requires technical skills.
                                It is in your interest to contact a professional microwave oven repair company to identify and rectify the problem in the
                                stirrer.</p>

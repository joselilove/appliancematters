                            <li>Transmission</li>
                            <li>Lid switch</li>
                            <li>Timer</li>
                            <li>Select switch</li>
                            <li>Water level switch.</li>
                            <p class="text-justify">Checking these parts is not easy and you may need some special tools such as multi-meter. If you are repairing the machine for the first time, it is in your interest to get help from an expert.</p>

                            <h5 class="font-weight-bold">Door Does Not Open After Cleaning Cycle</h5>
                            <p class="text-justify">The most likely cause of this problem is a faulty door switch or assembly. Before you check the condition of the switch or assembly, make sure you cut the power supply to the machine. If you don’t find any problem with these parts, you should check the oven temperature sensor. Checking the condition of the sensor is a complex procedure and must be done under the supervision of an expert.</p>

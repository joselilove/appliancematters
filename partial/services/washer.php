                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Does Not Start</h5>
                            <p class="text-justify">This can spell disaster in the lives of people who depend on a washing machine to complete their daily chores. If your machine does not start, you can start by checking the lid switch and actuator. You can also check the condition of:</p>
                            <li>The start switch</li>
                            <li>Door switch</li>
                            <li>Timer</li>
                            <p class="test-justify">If you don’t find any problem with these parts, you can check the knob, drive motor, and the main control board. This should help in solving the problem.</p>
                        </div>
                    </div>

                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Noisy Machine</h5>
                            <p class="text-justify">The toughest part about fixing your noisy dryer is identifying
                                the part that is causing the problem. Your machine may be noisy because of problems in the:</p>
                            <li>Drum glides</li>
                            <li>Bearing</li>
                            <li>Rollers</li>
                            <li>Shaft</li>
                            <li>Drive belt</li>
                            <li>Drum seal</li>
                            <li>Idler pulley</li>
                            <li>Blower</li>
                            <li>Wheel</li>
                            <li>Baffle</li>
                            <li>The motor.</li>
                            <p class="text-justify">Apart from these components, there are several other parts that can cause this problem.
                                Since identification and repair of a problem part is difficult, it is best to get technical help to solve this type of problem.</p>
                        </div>
                    </div>
                    <div class="row" id="wont_start">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Won’t Start</h5>
                            <p class="text-justify">There are several parts that can cause this problem.
                                You can start by checking the door and the rotary switch.
                                If there is no problem in these switches, you must check the following:</p>
                            <li>Thermal fuse</li>
                            <li>Drive motor</li>
                            <li>Timer</li>
                            <li>Terminal block</li>
                            <li>The main circuit board.</li>
                            <p class="text-justify">Checking the condition of each item and repairing or replacing them, can be a daunting task.
                                To make the job easier, you can get help from a professional appliance repair company.
                                These companies have technicians who have several years of experience in the field.</p>
                        </div>
                    </div>
                    <div class="row" id="heating_machine">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Heating Issues</h5>
                            <p class="text-justify">This could indicate a problem with any of the following:</p>
                            <li>The heating element</li>
                            <li>Thermostat</li>
                            <li>Igniter</li>
                            <li>Sensors</li>
                            <li>Gas control valve</li>
                            <li>Coils</li>
                            <li>Timer</li>
                            <p class="text-justify">Any of the above components can cause this issue. When checking the thermostat, you must check the condition of both cycling and high limit thermostat.
                                Most dryers come with temperature and radiant flame sensor, so make sure you check the condition of both these sensors.
                                If you still have issues, it is best to get professional help.</p>
                        </div>
                    </div>
                    <div class="row" id="does_not_tumbles">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Does Not Tumble</h5>
                            <p>This is another common problem that most users face. If your machine does not tumble, you must check the condition of the belt, pulley assembly, drive motor, and door switch.
                                If you have identified the problem, you could check the maintenance kit given by the manufacturer to get the required replacement parts.</p>
                        </div>
                    </div>
                    <div class="row" id="timer_does_not_advance">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">The Timer does not Advance</h5>
                            <p class="text-justify">If the timer for the dryer does not advance, there could be a problem in the timer motor or the cycling thermostat.
                                Timer motor is a small motor that is located in the control console of the machine.
                                To check the condition of the thermostat, you must check the power to the motor and the timer using a multi-meter.</p>
                        </div>
                    </div>
                    <div class="row" id="takes_too_long_dry">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Takes too long to Dry</h5>
                            <p class="text-justify">If the lint filter is blocked, it can restrict the airflow and increase dry time. Other causes of the problem include damaged:</p>
                            <li>Blower wheel</li>
                            <li>Gas valve coil</li>
                            <li>Felt drum seal</li>
                            <li>Door seal.</li>
                            <p class="text-justify">Check the condition of each part carefully and repair or replace them if necessary. Identifying and rectifying the problem is not easy. You will need help from an experienced repairman to solve this problem.</p>
                        </div>
                    </div>
                    <div class="row" id="does_not_shut_off">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Does Not Shut Off</h5>
                            <p class="text-justify">The dryer may not shut off because of a problem with the door switch, timer, cycling thermostat, or cool down thermostat. If there is a problem with the cool down thermostat, the drum of the machine will turn indefinitely until the door is opened.</p>
                        </div>
                    </div>
                    <div class="row" id="shots_off_too_soon">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Shuts off too Soon</h5>
                            <p class="text-justify">Most dryers use moisture sensors to control the timer when the automatic drying cycle is selected. If there is a problem with the sensors, the machine will shut off before the clothes can dry.

                                If you face any problem with your clothes dryer, it is best to allow professionals to handle the problem. DIY repairs are dangerous as you will be dealing with gas or electricity and should be attempted only under the supervision of the experts.</p>

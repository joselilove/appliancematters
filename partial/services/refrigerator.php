                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Noisy</h5>
                            <p class=text-justify>When there is a problem with the evaporator motor or condenser fan motor, your refrigerator will become noisy. Your appliance will become noisier when the refrigerator door is opened. If the noise appears to be coming from the freezer area, it means there is a problem with evaporator motor. Alternatively, if the noise is coming from the bottom, it means there could be a problem with the condenser fan motor.</p>
                        </div>
                    </div>
                    <div class="row" id="leaking_ref">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Leaking</h5>
                            <p class="text-justify">Your refrigerator may be leaking because of problems with any of the following:</p>
                            <li>Seals</li>
                            <li>Door gaskets</li>
                            <li>Water inlet valve</li>
                            <li>Ice maker assembly.</li>
                            <p class="text-justify">Air leakage in defective gaskets or seals can create in excessive moisture and potential water leaks. At times, cleaning the gasket will solve the problem. If this does not help, you may have to change the gasket. Problems with the inlet valve can avoided by inspecting the valve and by replacing or tightening the connectors. If the water is leaking from in or around the ice maker, you must check the ice maker assembly. This is a difficult process and you may have to get help from qualified technician to complete the task.</p>
                        </div>
                    </div>
                    <div class="row" id="does_not_start_ref">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Does Not Start</h5>
                            <p class="text-justify">A problem in the relay-start capacitor, electronic controls, or the overhead capacitor can cause this problem. You will need a multi-meter to check the condition of these parts. Before checking these parts, make sure to cut off the electric supply to the machine. If you have any experience in checking these electronic parts, it is in your interest to get help from an appliance repair company.</p>
                        </div>
                    </div>
                    <div class="row" id="not_making_ice_freeze">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Not making Ice</h5>
                            <p class="text-justify">If the ice maker does not make ice, you must inspect the water:</p>
                            <li>Fill tubes</li>
                            <li>The water inlet valve</li>
                            <li>Ice and water filter</li>
                            <li>Ice maker assembly, and the replacement ice maker</li>
                            <p class="text-justify">Checking the condition of each part is not easy. Getting help from a qualified and experienced repairman will ensure that your refrigerator starts making ice and helps you stay cool during summers.</p>
                        </div>
                    </div>
                    <div class="row" id="too_warm_ref">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Too Warm</h5>
                            <p class="text-justify">A problem with the air inlet damper or baffle can affect the air flow from the evaporator fan. If the damper is damaged, it can reduce the flow of cold air entering the fresh food compartment. This can increase the temperature inside the refrigerator. If you are inspecting the damper for the first time, you must be extremely careful as even a small mistake can damage the seal or the Styrofoam lining.</p>
                        </div>
                    </div>
                    <div class="row" id="does_not_dispense_water_ref">
                        <div class="col-md-1"></div>
                        <div class="col-md-11">
                            <h5 class="font-weight-bold">Does Not Dispense Water</h5>
                            <p class="text-justify">If your refrigerator does not dispense water, you must inspect these components:</p>
                            <li>Ice and water filter</li>
                            <li>The water inlet valve</li>
                            <li>Water dispenser actuator</li>
                            <li>Micro switch</li>
                            <li>Dispenser control board.</li>
                            <p class="text-justify">Apart from these parts, there are several other parts that can prevent the refrigerator from dispensing. Inspect each part carefully and get help if you are unable to identify the problem.</p>
                        </div>

                            <li>Shock absorbers</li>
                            <li>Tub dampening strap</li>
                            <li>Pump pulley</li>
                            <li>Tub bearing</li>
                            <li>Clutch assembly</li>
                            <li>Agitator directional cogs.</li>
                            <p class="text-justify">Checking the condition of each part can be difficult and time consuming. To make your easier, you could call an appliance repair company. These companies have several technicians who have experience in handling these situations.</p>
